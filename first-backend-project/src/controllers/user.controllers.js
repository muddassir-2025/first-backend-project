const generateAccessRefreshTokens= async(userId)=>{ //mongodb will auto give us an id
    try {
        const user=await User.findById(userId)

        if(!user){
            throw new ApiError(404,"User not found")
        }

        const accessToken=user.generateAccessToken() //in mongoose see func name
        const refreshToken=user.generateRefreshToken()
        //we need to store refreshTokens in our object so it remains logged in 

        user.refreshToken=refreshToken

        await user.save({validateBeforeSave: false})

        return {accessToken,refreshToken}

    } catch (error) {
        throw new ApiError(500, "something went wrong while generating tokens")
    }

}

